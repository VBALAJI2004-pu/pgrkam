export const getAllCategories = (): string[] => {
  const categories = new Set<string>();
  jobsData.forEach(job => categories.add(job.category));
  return Array.from(categories);
};

export const getJobsByCategory = (category: string) => {
  return jobsData.filter(job => job.category === category);
};

export const getJobById = (id: string) => {
  return jobsData.find(job => job.id === id);
};

export const searchJobs = (query: string) => {
  const lowerQuery = query.toLowerCase();
  return jobsData.filter(job => 
    job.title.toLowerCase().includes(lowerQuery) ||
    job.organization.toLowerCase().includes(lowerQuery) ||
    job.category.toLowerCase().includes(lowerQuery) ||
    job.location.toLowerCase().includes(lowerQuery)
  );
};
